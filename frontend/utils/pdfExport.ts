function printHTML(html: string, filename: string): void {
  const printWindow = window.open('', '_blank');
  if (printWindow) {
    printWindow.document.write(html);
    printWindow.document.close();
    
    // Wait for all content to render before printing
    printWindow.onload = () => {
      // Multiple checks to ensure content is fully rendered
      const checkAndPrint = () => {
        const body = printWindow.document.body;
        if (body && body.scrollHeight > 0) {
          printWindow.requestAnimationFrame(() => {
            setTimeout(() => {
              printWindow.focus();
              printWindow.print();
            }, 750);
          });
        } else {
          setTimeout(checkAndPrint, 100);
        }
      };
      checkAndPrint();
    };
  } else {
    console.error('Failed to open print window. Please allow popups for this site.');
    alert('Please allow popups to download PDF. Check your browser settings.');
  }
}

export async function shareCareerDocument(
  content: string, 
  title: string, 
  type: 'resume' | 'cover-letter' | 'interview-prep' | 'linkedin'
): Promise<void> {
  if (navigator.share) {
    try {
      await navigator.share({
        title: title,
        text: content,
      });
    } catch (err) {
      if ((err as Error).name !== 'AbortError') {
        console.error('Share failed:', err);
        fallbackShare(content, title);
      }
    }
  } else {
    fallbackShare(content, title);
  }
}

function fallbackShare(content: string, title: string): void {
  navigator.clipboard.writeText(content);
  alert('Content copied to clipboard! Share it on your preferred platform.');
}
